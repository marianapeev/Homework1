public class Task05 {
    public static void main (String[] args){
        System.out.println ("Ascending order:");
        for (int i =1; i <=100; i++){
            System.out.print (i+" ");
        }
        System.out.println("Descending order:");
        for (int i =100; i>=1; i--){
            System.out.print (i + " ");

        }


    }
}
